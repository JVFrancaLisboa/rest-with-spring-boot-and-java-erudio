package com.erudio.rest_with_spring_boot_and_java_erudio;

public class Greeting {

    private Long id;
    private String content;

    public Greeting(Long id, String content) {
        this.id = id;
        this.content = content;
    }

    public Long getId() {
        return id;
    }

    public String getContent() {
        return content;
    }
}
